	      <h3 class="pshadow hideme">Introduction</h3>
	      	<hr>
	          <p>KINOKI International Student Film Festival was founded in 2004 by students from the Universidad Iberoamericana to provide exposure for film students across the world. <br>
The name KINOKI is a word based in kino-pravda, a concept derived from the philosophy of soviet filmmaker Dziga Vértov. This philosophy consisted in capturing fragments of reality which, that when organized together, showed a deeper truth that could not be seen or reached by the naked eye.<br>
It’s been eleven years since KINOKI International Student Film Festival has promoted cinema culture and film student development with the exhibition of fiction, documentary research, animation and experimental short films. 
</p>		
